const _outgoing = new Map<string, RoadSegment[]>();
SEGMENTS.forEach(seg => {
  if (!_outgoing.has(seg.from)) _outgoing.set(seg.from, []);
  _outgoing.get(seg.from)!.push(seg);
});

export function getOutgoingSegments(nodeId: string): RoadSegment[] {
  return _outgoing.get(nodeId) || [];
}
