// Helper to create bidirectional segments
function biDir(
  id: string, from: string, to: string, name: string,
  speed: number, lanes: number, length: number,
  waypoints: [number, number][]
): RoadSegment[] {
  return [
    { id, from, to, name, speedLimit: speed, lanes, length, waypoints },
    { id: id + "_r", from: to, to: from, name: name + " (return)",
      speedLimit: speed, lanes, length, waypoints: [...waypoints].reverse() as [number, number][] },
  ];
}

